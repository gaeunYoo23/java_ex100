package java_ex_50_75;
import java.util.Scanner;
public class java_ex_68 {
	 public static void main(String[] args) {
	        Scanner scanner = new Scanner(System.in);
	        int num = scanner.nextInt();

	        for (int i = 0; i <= num; i++) {
	            System.out.println(i);
	        }

	        scanner.close();
	    }
}
